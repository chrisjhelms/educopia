<p>The Digital Collection of the Robert W. Woodruff Library consists of photographic images and publications from the Archives and Special Collections Department documenting the early history of the Atlanta University Center (AUC) institutions. These "founding documents" encompass pictures of AUC member institution presidents, faculty, students, alumni, earliest buildings, and an electronically searchable text of the Atlanta University Bulletin dating from 1883 to 1910.</p> 
